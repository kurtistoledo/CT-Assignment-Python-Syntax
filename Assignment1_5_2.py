# 5. Arithmetic Operations in Daily Life
# Task 2: Bank Interest

# Initial amount in the savings account
initial_amount = 2000  # Example initial amount
# Fixed yearly interest rate (expressed as a decimal)

interest_rate = 0.05  # 5% yearly interest rate
# Calculate the total amount in the account after a year

total_amount = initial_amount * (1 + interest_rate)
# Print the total amount after a year
print("Total in your savings account after a year:", total_amount)

# Total in your savings account after a year: 2100.0
